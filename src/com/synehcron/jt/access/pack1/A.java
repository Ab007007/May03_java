package com.synehcron.jt.access.pack1;

public class A
{
	private int x=0;
	int y=1;
	protected int z = 2;
	public int a =3;
	
	
	public static void main(String[] args)
	{
		A aObj = new A();
		System.out.println(aObj.x);
		System.out.println(aObj.y);
		System.out.println(aObj.z);
		System.out.println(aObj.a);
	}
			
}
